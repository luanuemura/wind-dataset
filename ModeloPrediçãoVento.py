import numpy as np
import pandas as pd
import joblib
from datetime import timedelta
from sklearn.preprocessing import RobustScaler
from sklearn.ensemble import RandomForestRegressor
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score
from sklearn.model_selection import TimeSeriesSplit

class WindPredictor:
    def __init__(self):
        self.model = None
        self.scaler = None
        self.features = None
        self.lags = [1, 2, 3, 4, 5]
        self.use_lags = True
        self.is_trained = False
        self.historical_data = None

    def prepare_features(self, df, is_training=True, use_lags=True):
        df_work = df.copy()
        df_work["DATE"] = pd.to_datetime(df_work["DATE"])
        df_work = df_work.sort_values("DATE").reset_index(drop=True)

        if use_lags:
            for lag in self.lags:
                df_work[f"WIND_lag{lag}"] = df_work["WIND"].shift(lag)

        # Sazonalidade
        df_work['month'] = df_work["DATE"].dt.month
        df_work['day_of_year'] = df_work["DATE"].dt.dayofyear
        df_work['month_sin'] = np.sin(2 * np.pi * df_work['month'] / 12)
        df_work['month_cos'] = np.cos(2 * np.pi * df_work['month'] / 12)
        df_work['day_sin'] = np.sin(2 * np.pi * df_work['day_of_year'] / 365)
        df_work['day_cos'] = np.cos(2 * np.pi * df_work['day_of_year'] / 365)

        # Interações
        if 'T.MAX' in df_work.columns and 'T.MIN' in df_work.columns:
            df_work['temp_range'] = df_work['T.MAX'] - df_work['T.MIN']
        if 'RAIN' in df_work.columns and 'T.MAX' in df_work.columns:
            df_work['rain_temp_interaction'] = df_work['RAIN'] * df_work['T.MAX']

        if self.features is None:
            basic_features = ["T.MAX", "T.MIN", "RAIN", "T.MIN.G"]
            lag_features = [f"WIND_lag{lag}" for lag in self.lags] if use_lags else []
            temporal_features = ['month_sin', 'month_cos', 'day_sin', 'day_cos']
            interaction_features = ['temp_range', 'rain_temp_interaction']
            self.features = [f for f in basic_features + lag_features + temporal_features + interaction_features if f in df_work.columns]

        return df_work

    def train(self, df, model_type='random_forest', save_path=None, use_lags=True, cv_splits=0, n_trials=10):
        import optuna
        from sklearn.linear_model import Ridge, Lasso, LinearRegression
        from sklearn.ensemble import RandomForestRegressor
        from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score
        from sklearn.preprocessing import RobustScaler
        from sklearn.model_selection import TimeSeriesSplit
        import numpy as np

        print(f"\nIniciando treinamento {'com lags' if use_lags else 'sem lags'}...")

        self.use_lags = use_lags
        self.features = None

        df_prepared = self.prepare_features(df, is_training=True, use_lags=use_lags)
        df_clean = df_prepared.dropna(subset=self.features + ['WIND'])

        print(f"Registros válidos: {len(df_clean)}")
        print(f"Features utilizadas: {self.features}")

        X = df_clean[self.features]
        y = df_clean['WIND']

        def get_model_and_search_space(trial, model_type):
            if model_type == 'random_forest':
                return RandomForestRegressor(
                    n_estimators=trial.suggest_int("n_estimators", 100, 500),
                    max_depth=trial.suggest_int("max_depth", 5, 30),
                    min_samples_split=trial.suggest_int("min_samples_split", 2, 10),
                    min_samples_leaf=trial.suggest_int("min_samples_leaf", 1, 5),
                    random_state=42, n_jobs=-1
                )
            elif model_type == 'xgboost':
                import xgboost as xgb
                return xgb.XGBRegressor(
                    n_estimators=trial.suggest_int("n_estimators", 100, 500),
                    max_depth=trial.suggest_int("max_depth", 3, 10),
                    learning_rate=trial.suggest_float("learning_rate", 0.01, 0.3),
                    subsample=trial.suggest_float("subsample", 0.5, 1.0),
                    colsample_bytree=trial.suggest_float("colsample_bytree", 0.5, 1.0),
                    random_state=42, n_jobs=-1
                )
            elif model_type == 'ridge':
                return Ridge(alpha=trial.suggest_float("alpha", 0.01, 100.0), random_state=42)
            elif model_type == 'lasso':
                return Lasso(alpha=trial.suggest_float("alpha", 0.001, 1.0), random_state=42)
            elif model_type == 'linear_regression':
                return LinearRegression()

        def objective(trial, X, y, model_type, n_splits):
            model = get_model_and_search_space(trial, model_type)
            tscv = TimeSeriesSplit(n_splits=n_splits)
            maes = []

            for train_idx, val_idx in tscv.split(X):
                X_train, X_val = X[train_idx], X[val_idx]
                y_train, y_val = y[train_idx], y[val_idx]

                scaler = RobustScaler()
                X_train_scaled = scaler.fit_transform(X_train)
                X_val_scaled = scaler.transform(X_val)

                model.fit(X_train_scaled, y_train)
                y_pred = model.predict(X_val_scaled)
                maes.append(mean_absolute_error(y_val, y_pred))

            return np.mean(maes)

        def run_optuna_search(X, y, model_type, n_trials, n_splits):
            study = optuna.create_study(direction="minimize")
            study.optimize(lambda trial: objective(trial, X, y, model_type, n_splits), n_trials=n_trials)
            print(f"Melhores hiperparâmetros encontrados para {model_type}: {study.best_params}")
            return study.best_params

        modelos_base = {
            'random_forest': lambda: RandomForestRegressor(n_estimators=200, max_depth=15,
                                                            min_samples_split=5, min_samples_leaf=2,
                                                            random_state=42, n_jobs=-1),
            'xgboost': lambda: __import__('xgboost').XGBRegressor(n_estimators=200, learning_rate=0.1,
                                                                max_depth=6, random_state=42, n_jobs=-1),
            'ridge': lambda: Ridge(alpha=10.0, random_state=42),
            'linear_regression': lambda: LinearRegression(),
            'lasso': lambda: Lasso(alpha=0.1)
        }

        if model_type != 'all':
            if cv_splits > 1:
                best_params = run_optuna_search(X.values, y.values, model_type, n_trials=30, n_splits=cv_splits)
                modelos_base[model_type] = lambda: get_model_and_search_space(optuna.trial.FixedTrial(best_params), model_type)
            modelos = {model_type: modelos_base[model_type]}
        else:
            modelos = modelos_base

        resultados = {}

        for nome, modelo_fn in modelos.items():
            print(f"\nTreinando modelo {nome}...")

            if cv_splits > 1:
                tscv = TimeSeriesSplit(n_splits=cv_splits)
                maes, rmses, r2s = [], [], []

                for fold, (train_idx, test_idx) in enumerate(tscv.split(X)):
                    X_train, X_test = X.iloc[train_idx], X.iloc[test_idx]
                    y_train, y_test = y.iloc[train_idx], y.iloc[test_idx]

                    self.scaler = RobustScaler()
                    X_train_scaled = self.scaler.fit_transform(X_train)
                    X_test_scaled = self.scaler.transform(X_test)

                    self.model = modelo_fn()
                    self.model.fit(X_train_scaled, y_train)

                    y_pred = self.model.predict(X_test_scaled)
                    maes.append(mean_absolute_error(y_test, y_pred))
                    rmses.append(np.sqrt(mean_squared_error(y_test, y_pred)))
                    r2s.append(r2_score(y_test, y_pred))

                    print(f"  [Fold {fold+1}] MAE: {maes[-1]:.4f} | RMSE: {rmses[-1]:.4f} | R²: {r2s[-1]:.4f}")

                mae, rmse, r2 = np.mean(maes), np.mean(rmses), np.mean(r2s)

            else:
                split_idx = int(len(df_clean) * 0.8)
                X_train, X_test = X.iloc[:split_idx], X.iloc[split_idx:]
                y_train, y_test = y.iloc[:split_idx], y.iloc[split_idx:]

                self.scaler = RobustScaler()
                X_train_scaled = self.scaler.fit_transform(X_train)
                X_test_scaled = self.scaler.transform(X_test)

                self.model = modelo_fn()
                self.model.fit(X_train_scaled, y_train)

                y_pred = self.model.predict(X_test_scaled)
                mae = mean_absolute_error(y_test, y_pred)
                rmse = np.sqrt(mean_squared_error(y_test, y_pred))
                r2 = r2_score(y_test, y_pred)

                print(f"{nome} - MAE: {mae:.4f} | RMSE: {rmse:.4f} | R²: {r2:.4f}")

            resultados[nome] = {
                'model_type': nome,
                'use_lags': use_lags,
                'MAE': mae,
                'RMSE': rmse,
                'R2': r2
            }

            if save_path:
                self.save_model(f"{save_path}_{nome}.pkl")
                print(f"Modelo salvo em: {save_path}_{nome}.pkl")

            self.historical_data = df_clean.copy()
            self.is_trained = True

        return resultados if model_type == 'all' else list(resultados.values())[0]


    def predict_single_day(self, target_date, weather_data):
        if not self.is_trained:
            raise ValueError("O modelo ainda não foi treinado. Treine-o com .train(...) antes de prever.")

        # Converter string para datetime, se necessário
        if isinstance(target_date, str):
            target_date = pd.to_datetime(target_date)

        # Criar linha de entrada com os dados fornecidos para essa data
        input_row = weather_data.copy()
        input_row["DATE"] = target_date
        input_row["WIND"] = np.nan  # pois queremos prever esse valor

        # Concatenar com histórico para calcular lags e outras features
        df_pred = pd.DataFrame([input_row])
        df_full = pd.concat([self.historical_data, df_pred], ignore_index=True)
        df_full = df_full.sort_values("DATE").reset_index(drop=True)

        # Regerar todas as features (lags, sazonais, etc.)
        df_prepared = self.prepare_features(df_full, is_training=False, use_lags=self.use_lags)

        # Selecionar a linha da data desejada
        row = df_prepared[df_prepared["DATE"] == target_date]
        if row.empty:
            raise ValueError("Não foi possível preparar os dados para a data especificada.")

        row = row.iloc[-1]

        # Verificar e preencher features faltantes, se necessário
        missing = [f for f in self.features if pd.isna(row[f])]
        for feature in missing:
            if feature in self.historical_data.columns:
                row[feature] = self.historical_data[feature].mean()
            else:
                row[feature] = 0.0  # fallback neutro para interações ou derivadas

        # Escalar e prever
        X = row[self.features].values.reshape(1, -1)
        X_scaled = self.scaler.transform(X)
        prediction = self.model.predict(X_scaled)[0]

        print(f"Previsão para {target_date.date()}: {prediction:.2f} knots")

        return {
            "date": target_date.strftime("%Y-%m-%d"),
            "predicted_wind_speed": round(prediction, 2),
            "missing_features": missing
        }


    def save_model(self, filepath):
        joblib.dump({
            'model': self.model,
            'scaler': self.scaler,
            'features': self.features,
            'lags': self.lags,
            'historical_data': self.historical_data,
            'use_lags': self.use_lags
        }, filepath)

    def load_model(self, filepath):
        data = joblib.load(filepath)
        self.model = data['model']
        self.scaler = data['scaler']
        self.features = data['features']
        self.lags = data['lags']
        self.historical_data = data['historical_data']
        self.use_lags = data.get('use_lags', True)
        self.is_trained = True
        print(f"Modelo carregado de: {filepath}")

    def explain_model(self, method='shap', num_samples=100):
        if not self.is_trained:
            raise ValueError("Modelo não treinado.")

        import shap
        from lime.lime_tabular import LimeTabularExplainer

        df = self.historical_data.dropna(subset=self.features + ['WIND'])
        X = df[self.features]
        y = df['WIND']

        X_scaled = self.scaler.transform(X)

        if method == 'shap':
            print("\nGerando explicações com SHAP...")
            explainer = shap.Explainer(self.model, X_scaled)
            shap_values = explainer(X_scaled[:num_samples])

            shap.summary_plot(shap_values, features=X.iloc[:num_samples], feature_names=self.features)

        elif method == 'lime':
            print("\nGerando explicações com LIME...")
            explainer = LimeTabularExplainer(X_scaled, feature_names=self.features,
                                            class_names=['WIND'], verbose=True,
                                            mode='regression')
            i = 0  # escolha da instância a ser explicada
            exp = explainer.explain_instance(X_scaled[i], self.model.predict, num_features=len(self.features))
            exp.show_in_notebook()
        else:
            raise ValueError("Método inválido. Use 'shap' ou 'lime'.")

